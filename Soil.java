class Soil{
static String soilTexture;
static String soilStructure;
static String soilDensity;
static String soilColor;
static void setSoilTexture (String  tE){
soilTexture = tE;
}
static String getSoilTexture (){
return soilTexture;
}
static void setSoilStructure (String  sT){
	soilStructure = sT;
}
static String getSoilStructure (){
	return soilStructure;
}
static void setSoilDensity(String dN){
	soilDensity = dN;
}
static String getSoilDensity () {
	return soilDensity;
}
static void setSoilColor(String cL){
	soilColor = cL;
}
static String getSoilColor(){
	return soilColor;
}
}
